    const newDirIndex = file.path.lastIndexOf("/");
    const oldDir = oldDirIndex === -1 ? "" : oldPath.substring(0, oldDirIndex);
    const newDir = newDirIndex === -1 ? "" : file.path.substring(0, newDirIndex);
