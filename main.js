function generateNanoID(size = ID_CONFIG.length) {
  let id = "";
  const bytes = new Uint8Array(size);
  crypto.getRandomValues(bytes);
  for (let i = 0; i < size; i++) {
    id += ID_CONFIG.alphabet[bytes[i] % ID_CONFIG.alphabet.length];
  }
  return id;
}

